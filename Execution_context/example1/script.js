function timesTen(a){ //timesTen function takes parameter a
    return a * 10; // parameter is returned to the line 7
}

let y = timesTen(x); // 1. timesTen function is called with parameter x (X has value of 10)
                     // 2. timesTen(x) is replaced with returned value and assigned to y
